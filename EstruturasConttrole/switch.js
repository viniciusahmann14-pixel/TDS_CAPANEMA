let dia = 2; //dia da semana
switch (dia) {
    case 1:
        console.log("Domingo");
        break;
    case 2:
        console.log("segunda-feira");
        break;
    case 3:
        console.log("terça-feira");
        break;
    case 4:
        console.log("quarta-feira");
        break;
    case 5:
        console.log("quinta-feira");
        break;
    case 6:
        console.log("sexta-feira");
        break;
    case 7:
        console.log("Domingo");
        break;
    default:
        console.log("número inválido!")
}